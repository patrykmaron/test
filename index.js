// function to upload video to S3
async function uploadToS3(videoBuffer, fileName) {
  const params = {
    Bucket: "drpcrd-dashboard",
    Key: `apify/${fileName}`,
    Body: videoBuffer,
  };

  return new Promise((resolve, reject) => {
    s3.upload(params, (err, data) => {
      if (err) {
        reject(err);
      } else {
        resolve(data);
      }
    });
  });
}

