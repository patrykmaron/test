const csvWriter = createCsvWriter({
  path: "out2.csv",
  header: [{ id: "json", title: "JSON" }],
});

// Function to append data to CSV
async function appendToCsv(data) {
  const records = [{ json: JSON.stringify(data) }];
  await csvWriter.writeRecords(records); // append the record
}

// function to analyze video using Rekognition
function analyzeVideo(bucket, videoName) {
  const params = {
    Video: {
      S3Object: {
        Bucket: bucket,
        Name: videoName,
      },
    },
    MinConfidence: 70.0,
    JobTag: videoName, // Using video name as JobTag to associate a video with its Rekognition job
  };

  return new Promise((resolve, reject) => {
    rekognition.startLabelDetection(params, function (err, data) {
      if (err) {
        reject(err);
      } else {
        resolve(data);
      }
    });
  });
}

